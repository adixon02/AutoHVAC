'use client';

import { ProjectInfo, BuildingInfo, Room, LoadCalculation, SystemRecommendation } from '../lib/types';
import { btuToTons } from '../lib/manualJ';
import { getSystemTypeDescription, formatEfficiency } from '../lib/systemRecommendations';
import { getClimateZone, defaultClimateZone } from '../lib/climateData';
import HVACLayoutViewer from './HVACLayoutViewer';

interface ResultsProps {
  projectInfo: ProjectInfo;
  buildingInfo: BuildingInfo;
  rooms: Room[];
  loadCalculation: LoadCalculation;
  recommendations: SystemRecommendation[];
  onStartOver: () => void;
}

export default function Results({
  projectInfo,
  buildingInfo,
  rooms,
  loadCalculation,
  recommendations,
  onStartOver
}: ResultsProps) {
  const climate = getClimateZone(projectInfo.zipCode) || defaultClimateZone;
  
  const handleDownloadPDF = async () => {
    try {
      const jsPDF = (await import('jspdf')).default;
      const doc = new jsPDF();
      
      // Header
      doc.setFontSize(20);
      doc.text('AutoHVAC Load Calculation Report', 20, 30);
      
      doc.setFontSize(12);
      let yPos = 50;
      
      // Project Info
      doc.text(`Project: ${projectInfo.projectName}`, 20, yPos);
      yPos += 10;
      doc.text(`Location: ${projectInfo.zipCode} (Climate Zone ${climate.zone})`, 20, yPos);
      yPos += 10;
      doc.text(`Type: ${projectInfo.projectType} - ${projectInfo.constructionType}`, 20, yPos);
      yPos += 20;
      
      // Building Info
      doc.setFontSize(14);
      doc.text('Building Information:', 20, yPos);
      yPos += 10;
      doc.setFontSize(11);
      doc.text(`Square Footage: ${buildingInfo.squareFootage} sq ft`, 25, yPos);
      yPos += 8;
      doc.text(`Stories: ${buildingInfo.stories}`, 25, yPos);
      yPos += 8;
      doc.text(`Insulation: ${buildingInfo.insulationQuality}`, 25, yPos);
      yPos += 8;
      doc.text(`Windows: ${buildingInfo.windowType} pane`, 25, yPos);
      yPos += 20;
      
      // Load Summary
      doc.setFontSize(14);
      doc.text('Load Calculation Summary:', 20, yPos);
      yPos += 10;
      doc.setFontSize(11);
      doc.text(`Total Cooling Load: ${loadCalculation.totalCoolingLoad.toLocaleString()} BTU/hr (${btuToTons(loadCalculation.totalCoolingLoad)} tons)`, 25, yPos);
      yPos += 8;
      doc.text(`Total Heating Load: ${loadCalculation.totalHeatingLoad.toLocaleString()} BTU/hr`, 25, yPos);
      yPos += 8;
      doc.text(`Sensible Cooling: ${loadCalculation.sensibleCooling.toLocaleString()} BTU/hr`, 25, yPos);
      yPos += 8;
      doc.text(`Latent Cooling: ${loadCalculation.latentCooling.toLocaleString()} BTU/hr`, 25, yPos);
      yPos += 20;
      
      // Room Breakdown
      doc.setFontSize(14);
      doc.text('Room-by-Room Loads:', 20, yPos);
      yPos += 10;
      doc.setFontSize(11);
      
      rooms.forEach((room, i) => {
        const load = loadCalculation.roomLoads[i];
        if (yPos > 260) {
          doc.addPage();
          yPos = 30;
        }
        doc.text(`${room.name}: ${load.coolingLoad.toLocaleString()} BTU/hr cooling, ${load.heatingLoad.toLocaleString()} BTU/hr heating`, 25, yPos);
        yPos += 8;
      });
      
      yPos += 10;
      
      // System Recommendations
      doc.setFontSize(14);
      if (yPos > 240) {
        doc.addPage();
        yPos = 30;
      }
      doc.text('System Recommendations:', 20, yPos);
      yPos += 10;
      doc.setFontSize(11);
      
      recommendations.forEach(rec => {
        if (yPos > 240) {
          doc.addPage();
          yPos = 30;
        }
        doc.text(`${rec.tier.toUpperCase()} Option:`, 25, yPos);
        yPos += 8;
        doc.text(`Type: ${rec.systemType}`, 30, yPos);
        yPos += 6;
        doc.text(`Cooling: ${(rec.coolingCapacity / 12000).toFixed(1)} tons`, 30, yPos);
        yPos += 6;
        doc.text(`Efficiency: ${formatEfficiency(rec.efficiency.seer, rec.efficiency.hspf)}`, 30, yPos);
        yPos += 6;
        doc.text(`Estimated Cost: $${rec.estimatedCost.total.toLocaleString()}`, 30, yPos);
        yPos += 12;
      });
      
      // Footer
      const pageCount = doc.internal.pages.length - 1; // -1 because first page is empty
      for (let i = 1; i <= pageCount; i++) {
        doc.setPage(i);
        doc.setFontSize(8);
        doc.text(`Generated by AutoHVAC - Page ${i} of ${pageCount}`, 20, 290);
        doc.text(new Date().toLocaleDateString(), 150, 290);
      }
      
      doc.save(`${projectInfo.projectName.replace(/\s+/g, '_')}_HVAC_Report.pdf`);
    } catch (error) {
      console.error('PDF generation failed:', error);
      alert('PDF generation failed. Please try again.');
    }
  };

  return (
    <div className="space-y-6">
      {/* Header */}
      <div className="card">
        <div className="flex justify-between items-start">
          <div>
            <h2 className="text-2xl font-bold text-hvac-navy mb-2">Load Calculation Results</h2>
            <p className="text-gray-600">
              {projectInfo.projectName} • {projectInfo.zipCode} • Climate Zone {climate.zone}
            </p>
          </div>
          <button onClick={handleDownloadPDF} className="btn-primary">
            Download Report
          </button>
        </div>
      </div>

      {/* HVAC Layout */}
      <HVACLayoutViewer 
        rooms={rooms}
        loadCalculation={loadCalculation}
        recommendations={recommendations}
      />

      {/* Load Summary */}
      <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
        <div className="card bg-blue-50 border-blue-200">
          <h3 className="text-lg font-semibold text-blue-900 mb-3">Cooling Load Summary</h3>
          <div className="space-y-2">
            <div className="flex justify-between">
              <span>Total Cooling Load:</span>
              <span className="font-bold">{loadCalculation.totalCoolingLoad.toLocaleString()} BTU/hr</span>
            </div>
            <div className="flex justify-between">
              <span>Tonnage Required:</span>
              <span className="font-bold">{btuToTons(loadCalculation.totalCoolingLoad)} tons</span>
            </div>
            <div className="flex justify-between text-sm text-gray-600">
              <span>Sensible:</span>
              <span>{loadCalculation.sensibleCooling.toLocaleString()} BTU/hr</span>
            </div>
            <div className="flex justify-between text-sm text-gray-600">
              <span>Latent:</span>
              <span>{loadCalculation.latentCooling.toLocaleString()} BTU/hr</span>
            </div>
          </div>
        </div>

        <div className="card bg-orange-50 border-orange-200">
          <h3 className="text-lg font-semibold text-orange-900 mb-3">Heating Load Summary</h3>
          <div className="space-y-2">
            <div className="flex justify-between">
              <span>Total Heating Load:</span>
              <span className="font-bold">{loadCalculation.totalHeatingLoad.toLocaleString()} BTU/hr</span>
            </div>
            <div className="flex justify-between">
              <span>Design Temperature:</span>
              <span className="font-bold">{climate.designTemperatures.winterDry}°F</span>
            </div>
          </div>
        </div>
      </div>

      {/* Room Breakdown */}
      <div className="card">
        <h3 className="text-lg font-semibold mb-4">Room-by-Room Load Breakdown</h3>
        <div className="overflow-x-auto">
          <table className="w-full">
            <thead>
              <tr className="border-b">
                <th className="text-left py-2">Room</th>
                <th className="text-right py-2">Area (sq ft)</th>
                <th className="text-right py-2">Cooling (BTU/hr)</th>
                <th className="text-right py-2">Heating (BTU/hr)</th>
              </tr>
            </thead>
            <tbody>
              {rooms.map((room, index) => {
                const load = loadCalculation.roomLoads[index];
                return (
                  <tr key={room.id} className="border-b">
                    <td className="py-2">{room.name}</td>
                    <td className="text-right py-2">{room.area}</td>
                    <td className="text-right py-2">{load.coolingLoad.toLocaleString()}</td>
                    <td className="text-right py-2">{load.heatingLoad.toLocaleString()}</td>
                  </tr>
                );
              })}
            </tbody>
          </table>
        </div>
      </div>

      {/* System Recommendations */}
      <div>
        <h3 className="text-xl font-bold mb-4">System Recommendations</h3>
        <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
          {recommendations.map((rec) => (
            <div key={rec.tier} className={`card ${
              rec.tier === 'standard' ? 'border-2 border-hvac-blue' : ''
            }`}>
              {rec.tier === 'standard' && (
                <div className="bg-hvac-blue text-white text-sm px-3 py-1 rounded-full inline-block mb-3">
                  Recommended
                </div>
              )}
              <h4 className="text-lg font-semibold mb-2">
                {rec.tier === 'ultra-premium' ? 'Ultra Premium' : rec.tier.charAt(0).toUpperCase() + rec.tier.slice(1)} Option
              </h4>
              <p className="text-sm text-gray-600 mb-4">
                {getSystemTypeDescription(rec.systemType)}
              </p>
              <div className="space-y-2 text-sm">
                <div className="flex justify-between">
                  <span>Cooling Capacity:</span>
                  <span className="font-semibold">{(rec.coolingCapacity / 12000).toFixed(1)} tons</span>
                </div>
                <div className="flex justify-between">
                  <span>Efficiency:</span>
                  <span className="font-semibold">{formatEfficiency(rec.efficiency.seer, rec.efficiency.hspf)}</span>
                </div>
                <div className="border-t pt-2 mt-2">
                  <div className="flex justify-between">
                    <span>Equipment:</span>
                    <span>${rec.estimatedCost.equipment.toLocaleString()}</span>
                  </div>
                  <div className="flex justify-between">
                    <span>Installation:</span>
                    <span>${rec.estimatedCost.installation.toLocaleString()}</span>
                  </div>
                  <div className="flex justify-between font-bold">
                    <span>Total Estimate:</span>
                    <span>${rec.estimatedCost.total.toLocaleString()}</span>
                  </div>
                </div>
              </div>
            </div>
          ))}
        </div>
      </div>

      {/* Actions */}
      <div className="flex justify-center gap-4 pt-6">
        <button onClick={onStartOver} className="btn-secondary">
          Start New Project
        </button>
      </div>
    </div>
  );
}